package com.company;

public class java_date {
    public static void main(String[] args) {
        // It shows time from 1 January 1970.
        System.out.println(System.currentTimeMillis()); //It shows time in MilliSecond
        System.out.println(System.currentTimeMillis()/1000/3600/24/365); //It shows year passed since 1970.
    }
}
